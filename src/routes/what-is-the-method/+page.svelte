    <h2>Method training with Jim</h2>
    <p>
      Method training with Jim O’Connor begins with a series of sensory exercises designed by Lee Strasberg to
      fundamentally train the actor’s instrument. Starting with the “cup” exercise, you will develop the
      sensory skills necessary to privately recall past events, people, places and things which evoke the
      emotional and psychological conditions like that of your character, thereby authentically meeting the
      demands of the play.
    </p>
    <p>
      You will learn the techniques to recover these rich experiences using sense memory and bring them back to
      life sensorially through taste, touch, smell, sight and sound. Bring your character to life by bringing
      your life to the character.
    </p>
    <p>
      Additionally, you will learn the basic grammar of acting as well as script analysis and character study,
      with specific instruction on how to apply Method acting techniques to scripted material. Freedom from
      tension, fear and self doubt is a crucial element in an actor’s work and special attention is given to
      these and all common acting problems. Jim enjoys teaching the Method and passing on this knowledge to
      other performing artists.
    </p>

    <h2>Where the Method comes from</h2>
    <p>
      The term “method” simply refers to a way of training and practicing a particular craft. The grandfather of
      Method acting was Konstantin Stanislavsky who developed a “system” which trains the actor to draw upon
      their own life experiences in the portrayal of a character. This groundbreaking work was brought to the
      American stage when Stanislavsky’s Moscow Art Theater toured New York City in the 1920’s.
    </p>
    <p>
      A group of young New York theater professionals, Lee Strasberg, Harold Clurman, Stella Adler, Sanford
      Meisner and Cheryl Crawford were floored by this organic work and immediately began training in this
      system. Later, they formed their own company, The Group Theater, which went on to revolutionize modern
      acting.
    </p>
    <p>
      After the Group’s disbandment, Lee Strasberg took the helm at The Actor’s Studio where he continued to
      hone the Method and train industry greats such as Marlon Brando, Robert De Niro, Ellen Burstyn, James
      Dean, Dustin Hoffman, Sally Field, Al Pacino, Paul Newman, Jane Fonda and Marilyn Monroe.
    </p>

    <h2>Why it’s misunderstood</h2>
    <p>
      Today, Method acting is largely misunderstood due to ignorance. The only way to learn the Method is
      through the real-time practice of guided exercises. Additionally, most Hollywood media outlets obfuscate
      poor behavior as somehow the result of an acting exercise.
    </p>

    <h2>Storytelling & the actor</h2>
    <p>
      Storytelling is why human beings have been so successful as a species. It’s in our DNA. This practice
      allows for the transfer of vital information from one generation to the next, not only for basic survival
      but also for an understanding of the human condition. Humans cannot live on bread alone. The arc of human
      life includes an insatiable appetite for meaning and purpose. These values are mined by the artist and
      shared with an audience.
    </p>
    <p>
      Plays, movies, novels, poems and songs all tell a story and carry a message. Actors explore the friction
      of life in search of a beautiful understanding, starting with themselves.
    </p>
